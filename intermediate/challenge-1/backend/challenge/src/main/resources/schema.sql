-- Table T_DRIVER
DROP TABLE IF EXISTS T_DRIVER;
CREATE TABLE T_DRIVER (
	F_SSN VARCHAR2(50),
	F_FIRSTNAME VARCHAR2(25) NOT NULL,
	F_LASTNAME VARCHAR2(25) NOT NULL,
	PRIMARY KEY (F_SSN)
);

-- Table T_BUS
DROP TABLE IF EXISTS T_BUS;
CREATE TABLE T_BUS (
	F_BUSID INTEGER,
	F_CAPACITY INTEGER,
	F_MODEL VARCHAR2(25) NOT NULL,
	F_MAKE VARCHAR2(25) NOT NULL,
	F_SSN VARCHAR2(50),
	PRIMARY KEY (F_BUSID),
	FOREIGN KEY (F_SSN) REFERENCES T_DRIVER (F_SSN)
);

-- Table Schedule
DROP TABLE IF EXISTS T_SCHEDULE;
CREATE TABLE T_SCHEDULE (
	F_BUSID INTEGER,
	F_SSN VARCHAR2(50),
	F_DATEFROM DATE NOT NULL,
	F_DATETO DATE NOT NULL,
	PRIMARY KEY (F_BUSID, F_SSN)
);